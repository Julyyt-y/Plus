package com.example.kaoyan.tagcloud;

public class Item {

    private String title;

    private String titleItem;

    public Item(String title, String titleItem) {
        this.title = title;
        this.titleItem = titleItem;
    }

    public String getTitle() {
        return title;
    }

    public String getTitleItem() {
        return titleItem;
    }

}
